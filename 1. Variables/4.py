# Start with a name
player_name = "Jordan"
print("Initial player:", player_name) # Output: Initial player: Jordan

# Reassign the variable to a new name
player_name = "Pippen"
print("New player:", player_name) # Output: New player: Pippen

# --- Example of changing the data type ---

# x starts as a number (integer)
x = 100
print("x is:", x) # Output: x is: 100

# Now, reassign x to be text (string)
x = "one hundred"
print("Now, x is:", x) # Output: Now, x is: one hundred