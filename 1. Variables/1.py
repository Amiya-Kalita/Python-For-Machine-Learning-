# A variable to store a whole number (an integer)
my_age = 30
print(my_age) # Output: 30

# A variable to store text (a string)
# Text must be in single ' ' or double " " quotes
my_name = "Alex"
print(my_name) # Output: Alex

# A variable to store a number with a decimal point (a float)
item_price = 19.95
print(item_price) # Output: 19.95

# A variable to store a True or False value (a boolean)
# Note the capital T and F
is_learning_python = True
print(is_learning_python) # Output: True