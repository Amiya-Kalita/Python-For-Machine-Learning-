# Using variables in print statements
first_name = "Maria"
country = "Brazil"

# You can print multiple variables
print("My name is", first_name, "and I am from", country)
# Output: My name is Maria and I am from Brazil

# Using variables in calculations
price = 150.0
tax_rate = 0.05

# Calculate the tax
tax_amount = price * tax_rate
print("Tax amount is:", tax_amount) # Output: Tax amount is: 7.5

# Calculate the total price
total_price = price + tax_amount
print("The total price is:", total_price) # Output: The total price is: 157.5