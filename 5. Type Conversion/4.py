my_int = 45
my_float = float(my_int)

print(f"Integer value: {my_int}") # Output: 45
print(f"Converted float value: {my_float}") # Output: 45.0

str_num1 = "200"
str_num2 = "78.54"

float1 = float(str_num1)
float2 = float(str_num2)

print(f"Converted from '200': {float1}") # Output: 200.0
print(f"Converted from '78.54': {float2}") # Output: 78.54